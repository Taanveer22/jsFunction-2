// example 03
function doMath(n1, n2){
    let sum = n1 + n2;
    let diff = n1 - n2;
    let multi = sum * diff;
    let answer = multi / 2;
    return answer;
} 

console.log(doMath(10, 5));
console.log(doMath(20, 40));