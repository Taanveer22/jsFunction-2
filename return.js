// example 01
function tenTimes (number) {
    let multiplication = number * 10;
    return multiplication;
}
console.log(tenTimes);
console.log(tenTimes());
console.log(tenTimes(5));
const tenTimesOutput = tenTimes(7);
console.log(tenTimesOutput);


// example 02
function cutHalf(num) {
    let divided = num / 2;
    return divided;
}
console.log(cutHalf);
console.log(cutHalf());
console.log(cutHalf(30));
const cutHalfOutput = cutHalf(600);
console.log(cutHalfOutput);