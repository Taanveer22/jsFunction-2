// example 01
function isEven(number){
    if( number % 2 === 0) {
        return true;
    }
    else{
        return false;
    }
}
console.log(isEven(4));
console.log(isEven(7));


// example 02
function isOdd(num){
    if(num % 2 === 1){
        return true;
    }
    return false;
}
console.log(isOdd(3));
console.log(isOdd(6));